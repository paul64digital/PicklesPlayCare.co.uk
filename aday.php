<p class="subtitle">A Day at Pickles Playcare</p>
<p>During your child's day at Pickles Playcare he/she will arrive to a warm welcome from myself and their playmates.  He/she will have the opportunity to take part in 'quiet' play whilst I welcome the other children and prepare breakfast for everybody.  Breakfast is a time for me to sit down with the children and ask them about their previous evening with Mummy and Daddy or about what they did at the weekend depending on the day of the week.  This allows the children to develop their social skills by talking as part of a group.  Breakfast is always eaten at the table with the younger children close by in a highchair.  This also encourages social skills as it makes them all feel like part of the group.</p>
<p>After breakfast we enjoy a wide range of activities such as, an outing to the local park, painting, drawing, building with bricks/lego, reading, dancing, making music, role play, cooking etc.  I always try to find out what the children would like to play with rather than always deciding for them, this encourages them to make decisions and choices independently.  The morning is when I usually encourage the children to take part in 'Focus' activities as it is when the children are most likely to have lots of energy.</p>
<p>Mid morning we sit at the table for a snack.</p>
<p>After a lively morning of play we wash our hands and sit down at the table for some lunch, I make the same lunch for all of the children unless they're allergic to what I have made or are simply too young to eat what is on the menu.  An alternative lunch will be prepared for such children.</p>
<p>After lunch we wash our hands and sit down for story time together before I change nappies and children who need it go for an afternoon nap.  Children who do not need to sleep will have to opportunity to do quiet activities and to relax for a while.</p>
<p>Once everybody is rested and awake we carry on with all sorts of activities and the children have another snack.  Occasionally at this time of the day the children will be able to watch a small amount of children's television or a children's DVD.</p>
<p>After play time is dinner time, this is the time of day when I try to wind the day down by talking about what we have done during the day.  Dinner is always a hot, home cooked meal that is cooked with fresh, healthy ingredients wherever possible.</p>
<p>After dinner is yet more playing before all of the children are collected.  Each child takes home their Daily book for you to look over so that you can read all about your child's very busy day at Pickles Playcare.</p>

